function miFuncion(a,b){
    console.log("Suma: " + (a+b));
}

//Primero se declara la funcion
//Segundo se la llama

miFuncion(3,4);

/*   HOISTING 
Podemos definir la funcion en cualquier parte del programa
Y la vamos a poder llamar, ya sea antes o despues de definirla

El concepto de Hoisting mueve la declaracion de la funcion al inicio del programa
Para que la podamos utilizar en cualquier parte de nuestro archivo
*/